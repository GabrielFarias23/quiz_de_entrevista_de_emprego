    private void jButtonEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEntrarActionPerformed
   String email = jTextFieldLogin.getText();
    String senha = new String(jPasswordFieldSenha.getPassword());

try {
        LoginController controller = new LoginController();
        boolean autenticado = controller.autenticar(email, senha);

        if (autenticado) {
            JOptionPane.showMessageDialog(null, "Login realizado com sucesso!");
            
            // Abre a tela do quiz
            TelaQuiz telaQuiz = new TelaQuiz();
            telaQuiz.setVisible(true);
            
            // Fecha a tela de login
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Email ou senha inv√°lidos.");
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Erro ao tentar logar: " + e.getMessage());
        e.printStackTrace(); // imprime o erro no console   
    }
    }//GEN-LAST:event_jButtonEntrarActionPerformed

